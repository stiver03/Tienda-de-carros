    private void jbtnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGrabarActionPerformed
        
        ControlAutomovil auto= ControlAutomovil.getInstance();
        
        String placa=jtxtPlaca.getText();
        String nombre=jTextField2.getText();
        String apellido=jTextField3.getText();
        String cedula=jTextField4.getText();
        String telefono=jTextField5.getText();
        
        
        try{
            if(!placa.equals("") && auto.verificarCarro(placa) && 
                !nombre.equals("")&& !apellido.equals("")
                && !cedula.equals("")&& !telefono.equals(""))
            {
                ControlCliente cliente= ControlCliente.getInstance();
                cliente.venderAuto(nombre, apellido, cedula, telefono, placa);
                JOptionPane.showMessageDialog(this,"venta exitosa");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"venta no realizada, falta de datos \n carro no disponible");
        }
        
       
    }//GEN-LAST:event_jbtnGrabarActionPerformed

