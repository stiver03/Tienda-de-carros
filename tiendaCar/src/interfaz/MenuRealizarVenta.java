    private void jbtnGrabar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGrabar1ActionPerformed
    try
        {
            ControlAutomovil auto= ControlAutomovil.getInstance();
            String placa=jtxtPlaca.getText();
        
            if(!placa.equals("") && auto.verificarCarro(placa))
            {
            
                JOptionPane.showMessageDialog(this,
                    "informacion del auto con placas "+placa+
                        "\n"+"marca "+auto.getMarca()+
                        "\n"+"precio "+auto.getPrecio()+
                        "\n"+"modelo "+auto.getModelo()+
                        "\n"+"color "+auto.getColor());
            
            }
           }catch(Exception e){
            JOptionPane.showMessageDialog(this,"carro no registrado");
        }
      
    }//GEN-LAST:event_jbtnGrabar1ActionPerformed

